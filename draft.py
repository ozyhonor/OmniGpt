import re

data = [
    "10 - Коммутативная алгебра. Теория множеств и теория групп",
    "15 - Некорректное число",
    "5 - Идемпотентные операции в математике",
    "20 - Нейтральные элементы и их свойства",
    "1 - Определение множества и его свойства",
    "Без числа - строка без чисел"
]

# Сортируем строки по первому числу, если числа нет или оно вне диапазона (0-10) — используем -бесконечность
sorted_data = reversed(sorted(data, key=lambda x: int(re.search(r'\d+', x).group()) if re.search(r'\d+', x) and 0 <= int(re.search(r'\d+', x).group()) <= 10 else float('-inf')))

for line in sorted_data:
    print(line)
